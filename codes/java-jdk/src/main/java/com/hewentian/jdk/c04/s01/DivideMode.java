package com.hewentian.jdk.c04.s01;

public class DivideMode {
    public static void main(String args[]) {
        System.out.println("17.0/2 = " + 17.0 / 2);
        System.out.println("17/2 = " + 17 / 2);

        System.out.println("17.1 mod 4 = " + 17.1 % 4);
        System.out.println("17 mod 4 = " + 17 % 4);
    }
}
